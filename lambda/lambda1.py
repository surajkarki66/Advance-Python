# Lambda function in python is the anonymous function. Because this function has no name thats why it is
# called anonymous function.

# Lambda function is also called one liner function.

# Functions are also objects in the python so we can pass function in objects.

# A function to find square of number.
num_square = lambda x : x*x

result = num_square(5)
print(result)


num_add = lambda x, y : x+y

result = num_add(1, 2)
print(result)